package section1.part1;

public class Class11ArrExp {
    public static void main(String[] args) {
        double result = 10 / 3;
        System.out.println(result);

        int x = 1;
        int y = ++x;
        System.out.println(x + "  " + y);

        int z = x + 2;
        z += 2;
        System.out.println(z);
    }
}

